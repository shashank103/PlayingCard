abstract class BaseCard {

    String suit;
    String pip;
    public String getSuit()
    {
        return this.suit;
    }
    public String getPip()
    {
        return this.pip;
    }
    public  void setSuit(String s)
    {
        suit=s;
    }
    public  void setPip(String p)
    {
        pip=p;
    }
}

